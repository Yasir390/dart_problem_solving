// ржзрж░рж╛ ржпрж╛ржХ gcd(56, 98) ржмрзЗрж░ ржХрж░ржм:
// 98 % 56 = 42 тЖТ ржирждрзБржи pair (56, 42)
// 56 % 42 = 14 тЖТ ржирждрзБржи pair (42, 14)
// 42 % 14 = 0 тЖТ рж╢рзЗрж╖
// ЁЯСЙ GCD = 14

int gcd(int a, int b) {
  while (b != 0) {
    int temp = b;
    b = a % b;
    a = temp;
  }
  return a;
}

void main() {
  print("GCD of 12 and 18 is ${gcd(12, 18)}");
  print("GCD of 56 and 98 is ${gcd(56, 98)}");
}
